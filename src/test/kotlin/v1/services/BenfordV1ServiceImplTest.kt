    fun `happy case when calling analyze`() {
        val request =
            BenfordAnalyzeV1Request(
                text = "123 456 789 101 112 131 415 161 718 192",
                significanceLevel = 0.05,
            )
        val response = service.analyze(request)

        assertNotNull(response)
        assertEquals(9, response.expectedDistribution.size)
        assertEquals(9, response.actualDistribution.size)
        assertTrue(response.chiSquareValue >= 0)
    }

